                <h1 class="text-center fw-bold blockchainTitle p-5 col-8 mx-auto rounded-5">La Blockchain</h1>
                <div class="col-8 mx-auto">
                    <p class="fs-2 text-white" id="introduction">Qu'est ce que c'est ?</p>
                    <div>
                        <div>
                            <img src="images/Blockchain.jpg" class="rounded-4 float-start me-4 mb-4 blockChainImg">
                        </div>
                        <div>
                            <p>
                                Une blockchain (chaîne de blocs) est une technologie de stockage et de transmission d'informations, transparente, sécurisée, et fonctionnant sans organe central de contrôle, c'est à dire qu'elle se gère elle-même.
                                Il s'agit d'une base de données distribuée dont les informations envoyées par les utilisateurs et les liens internes à la base sont vérifiés et groupés à intervalles de temps réguliers en blocs, 
                                l'ensemble étant sécurisé par cryptographie, et formant ainsi une chaîne.
                                De plus, la blockchain contient dans sa base de données tout l'historique des échanges effectués par ses utilisateurs depuis sa création. Cette base de données est sécurisée et distribuée.
                            </p>
                            <p>
                                Lorsque l'on parle de la blockchain, on l'associe souvent à la crypto monnaie tel que le bitcoin, le plus connu, mais aussi l'Ethereum et d'autres encore. Mais de plus en plus la blockchain se diversifie et s'implante 
                                dans d'autres domaines que la crypto-monnaie. On considère que la première blockchain est apparue en 2008. Elle a été développée par une personne qui utilise le pseudonyme Satoshi Nakamoto. La blockchain a été implémentée 
                                l'année suivante par Satoshi Nakamoto en tant que composant principal du bitcoin, où elle sert de registre public à toutes les transactions sur le réseau.
                            </p>
                        </div>
                    </div>
                    <p class="d-block mt-5">
                        Il existe 2 types de blockchain :
                        <br>
                        • Les blockchains de type privées sont limitées à un certain nombre de personnes.
                        <br>
                        • Les blockchains de type publiques sont ouvertes à tous.
                        <br><br>
                        La blockchain a vu le jour avec l'année d'après le bitcoin est donc la blockchain est en général assimilée à la monnaie numérique mais aujourd'hui de nombreux acteurs (entreprises, gouvernements, etc) 
                        envisagent l'utilisation de la technologie blockchain pour d'autres cas que la monnaie numérique. Le caractère décentralisé de la blockchain, ainsi que connu par sa sécurité et sa transparence, promet 
                        des applications bien plus larges que le domaine monétaire.
                    </p>
                </div>
                <div class="col-8 mx-auto">
                    <p class="fs-2 text-white">Comment ça marche ?</p>

                    <p>
                        Pour commencer la blockchain utilise le modèle de réseau P2P (paire à paire), cela définit un modèle de réseau informatique d'égal à égal entre ordinateurs, les ordinateurs distribuent et reçoivent une 
                        données ou un fichier. Dans ce type de réseau, comparable au réseau client-serveur, chaque client devient lui-même un serveur. Le P2P permet à plusieurs ordinateurs de communiquer entre eux via un réseau». 
                        En effet, la blockchain est une technologie de stockage et de transmission d'informations entre particuliers. Elle fonctionne sans organe central de contrôle.
                    </p>

                    <p class="fs-3 text-white fw-bold" id="decentralisation">
                        1. Décentralisation :
                    </p>
                    <p>
                        Le principal atout de la blockchain est sa décentralisation. Cela veut dire qu'elle n'est pas centralisée à un seul ou quelques endroits comme c'est le cas pour un système bancaire par exemple. La blockchain 
                        est stockée chez ses propres utilisateurs. Par exemple celle utilisée par le Bitcoin (la plus connue des crypto-monnaies) est stockée chez environ 1.000.000 utilisateurs en même temps.
                    </p>
                    <div>
                        <div>
                            <img src="images/graphes.png" class="float-start me-4 graphesImg">
                        </div>
                        <div>
                            <p>
                                Il n'est pas nécessaire qu'une personne physique vérifie chaque transaction en vérifiant la disponibilité de la somme d'argent et l'intention de l'expéditeur. C'est pourquoi contrairement à un 
                                virement bancaire qui dure plusieurs jours, une transaction sur une blockchain peut être quasi instantanée si le réseau est rapide et non surchargé. C'est une révolution de la transaction.
                            </p>
                            <p>
                                Dans le schéma on représente 3 types de réseaux. La blockchain est identique au troisième réseau. On peut constater qu'il n'y a pas de centre vers lequel vont toutes les transactions. De plus, 
                                chaque point (utilisateur) est directement ou indirectement relié à tous les autres points. De ce fait, on peut se passer d'un quelconque intermédiaire de confiance. L'intermédiaire de 
                                confiance est remplacé par toutes les personnes du réseau. 
                            </p>
                        </div>
                    </div>

                    <p class="fs-3 text-white fw-bold" id="historiqueDesDonnees">
                        2. historique des données :
                    </p>
                    <p>
                        Toutes les informations depuis la création d'une blockchain sont stockées chez les utilisateurs.
                        C'est comparable à une même base de données* stockée sur plusieurs ordinateurs, ou à un grand registre de comptes commun à tous. Ainsi, il y a une notion de transparence. Comme tout est enregistré et souvent publique, 
                        il n'est pas possible de cacher des transactions. C'est utile pour rassurer des clients en leur prouvant que le parcours parcouru par un produit alimentaire est bien vrai, ou encore pour démontrer qu'une entreprise a 
                        bien versé des fonds à une association comme promis. L'historique non modifiable est donc un atout pour la blockchain.
                    </p>

                    <p class="fs-3 text-white fw-bold" id="verification">
                        3. Vérification :
                    </p>
                    <p>
                        Le processus de validation et de sécurisation des transactions est assuré par les nœuds du réseau blockchain appelés « mineurs »*, qui s'appuient sur la copie identique du registre qu'ils possèdent et la transaction en cours.<br>
                        Pour fonctionner. la blockchain a besoin de jetons. Les transactions sont regroupées par blocs pour une meilleure efficacité. Par exemple, un bloc de la blockchain du Bitcoin peut contenir entre 1000 et 3000 transactions.<br>
                        Un bloc contient plusieurs informations : le numéro de version du Bloc, l'en-tête chiffrée du bloc précédent, le nombre de secondes écoulés depuis le 1970-01-01T00:00 UTC pour calculer l'heure de la transaction, un objectif de difficulté 
                        pour la preuve de travail et un nombre aléatoire d'une taille de 32-bit.
                        <br><br>

                        Dans un bloc il y a aussi une somme de contrôle. Elle contient l'identifiant de bloc précédent et la somme de contrôle du bloc précédent. Cette somme de contrôle est une clé unique : c'est le résultat du chiffrement* des informations citées 
                        précédemment. Grâce à cette somme, il suffit d'avoir un hash pour retrouver un bloc, et il est possible de remonter jusqu'au premier bloc à partir de n'importe quel bloc en vérifiant la validité. En effet, un bloc n'est pas valide s'il y a un 
                        changement car même si le changement est minime, le hash changera complètement donc il n'est pas possible de tricher. La somme de contrôle est toujours vérifiée pour détecter une quelconque modification du bloc.<br>
                        Si l'auteur de la modification a aussi modifié l'en-tête du bloc en y incluant sa nouvelle somme de contrôle, il a modifié en même temps son identifiant. Les blocs qui lui faisaient référence n'ont donc plus de prédécesseur et la blockchain 
                        est rompue. Introduire dans la chaîne un bloc modifié forcerait donc à modifier les en-têtes de tous les blocs suivants, une opération d'autant plus coûteuse que le bloc est plus ancien, et que le calcul des sommes de contrôle est lui-même 
                        rendu plus consommateur de ressources par l'imposition de contraintes très sévères<br>
                        Cette structure de fichier est bien adaptée à l'enregistrement sécurisé d'écritures critiques produites en continu et ayant valeur juridique, comme les transactions de paiement, mais également de nombreux autres types d'écritures comme des 
                        titres de propriété, des brevets, des diplômes, etc<br>
                    </p>

                    <p class="fs-3 text-white fw-bold" id="cryptographie">
                        4. Cryptographie :
                    </p>
                    <div>
                        <img src="images/cryptograhie.jpg" class="float-start me-4 graphesImg">
                    </div>
                    <div>
                        <p>
                            Le chiffrement (ou "cryptage") est un procédé de cryptographie grâce auquel on souhaite rendre la compréhension d'un document impossible à toute personne qui n'a pas la clé de (dé)chiffrement. 
                            Ce principe est généralement lié au principe d'accès conditionnel.<br>
                            La blockchain est chiffrée pour sa sécurité. Cela veut dire qu'il faut une clé secrète pour pouvoir lire les messages échangés. Il est question d'utiliser 2 clés différents pour chiffrer et déchiffrer, 
                            on dit que la cryptographie utilisée est asymétrique. L'avantage est qu'il n'y a aucun secret à partager car chacun a sa propre clé.
                        </p>
                        <p>
                            Chaque utilisateur a une clé publique et une clé privée. La clé privée de l'émetteur sert à signer le message pour prouver que c'est bien lui qui l'envoie. Il est le seul à pouvoir le faire car il 
                            est le seul à disposer de cette clé privée. Ensuite, la clé publique du récepteur est utilisée par lui-même pour chiffrer le message pour qu'il ne soit lisible que par lui. Pour déchiffrer le message,
                            il doit utiliser sa clé privée associée à sa clé publique. Pour être sûr de l'émetteur du message, il faut vérifier la signature en comparant la clé publique de l'émetteur et sa signature. Il y a donc besoin 
                            que d'adresses chiffrées, pas de nom, prénom, âge ... Ce qui en fait un réseau anonyme.
                        </p>
                        <p>
                            L'algorithme de hash utilisé par la blockchain du Bitcoin est le SHA256. Son nom provient de Secure Hash Algorithm (algorithme sécurisé de chiffrement) et 256 car un message en SHA256 est composé de 256 bits, 
                            soit 32 octets. Il a été inventé par la NSA. Par exemple, si l'on chiffre «a» en SHA256, cela donne «ca978112ca1bbdcafac231b39a23dc4da786eff8147c4e72b9807785afee48bb». L'avantage est qu'il est quasiment 
                            impossible de retrouver le message d'origine à partir du message chiffré.
                        </p>
                    </div>

                    <p class="fs-3 text-white fw-bold" id="minage">
                        5. Minage :
                    </p>
                    <p>
                        Les mineurs sont les utilisateurs d'une blockchain qui contribuent à la génération et vérification des blocs de transactions en mettant à disposition leur puissance de calcul. Ils gagnent un dividende à chaque transaction 
                        en fonction de leur puissance de calcul. Cette puissance est appelée hash rate, c'est-à-dire taux de chiffrement. Il peut y avoir une quantité infinie de mineurs. Le premier mineur qui finit la génération d'un bloc est le 
                        plus rémunéré. Ensuite, le bloc est envoyé aux autres mineurs qui vérifieront le bloc généré.
                    </p>
                </div>

